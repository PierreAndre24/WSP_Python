def OptimalSubFig(n_subfig):
    if n_subfig == 0:
        t = [0,0]
    elif n_subfig == 1:
        t = [1,1]
    elif n_subfig == 2:
        t = [1,2]
    elif n_subfig == 3:
        t = [1,3]
    elif n_subfig == 4:
        t = [2,2]
    elif n_subfig == 5:
        t = [2,3]
    elif n_subfig == 6:
        t = [2,3]
    return t
